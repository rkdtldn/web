const num1 = Number(prompt("첫번째 숫자를 입력하세요."));
const num2 = Number(prompt("두번째 숫자를 입력하세요."));


let result = num1 + num2;
console.log("num1 + num2 = " + result);

result = num1 - num2;
console.log("num1 - num2 = " + result)

result = num1 * num2;
console.log("num1 * num2 = " + result)

result = num1 / num2;
console.log("num1 / num2 = " + result)

result = num1 % num2;
console.log("num1 % num2 = " + result)

result = num1 ** num2;
console.log("num1^num2 = " + result)

